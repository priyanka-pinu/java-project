package Decisionmaking;

public class Ifelseif2 {

	public static void main(String[] args) {

		int marks=80;
		if (marks>80) {
			System.out.println("Topper");
		}
		else if(marks<80 && marks>=60) {
			System.out.println("first");
		}
		else {
			System.out.println("second");
		}
	}

}
