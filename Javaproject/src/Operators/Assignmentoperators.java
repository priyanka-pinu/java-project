package Operators;

public class Assignmentoperators {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c=0;
		
		c=a+b;
		System.out.println("c=a+b="+c);
		
		c+=a;
		System.out.println("c+=a"+c);
		
		c-=a;
		System.out.println("c-=a"+c);
		
		c*=a;
		System.out.println("c*a="+c);
		
		a=5;
		b=10;
		c/=a;
		System.out.println("c/a="+c);

	}


	}


